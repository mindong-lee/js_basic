// typeof : 데이터 타입을 확인
// js is a dynamic , weakly typed programming language

let variable;
console.log(typeof variable);

variable = '';
console.log(typeof variable);

variable = 5;
console.log(typeof variable);

variable = '5';
console.log(typeof variable);

variable = true;
console.log(typeof variable);

variable = null;
console.log(typeof variable);

variable = undefined;
console.log(typeof variable);

variable = {};
console.log(typeof variable);

variable = function(){};
console.log(typeof variable);

variable = Symbol();
console.log(typeof variable);